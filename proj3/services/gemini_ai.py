def _call_gemini(client, prompt, temperature=0.2, max_retries=3):
    """Gemini API 호출 (429는 대기 후 재시도, 503은 백업 모델)"""
    models = ['gemini-2.5-flash', 'gemini-2.0-flash', 'gemini-2.0-flash-lite']
    for model in models:
        for attempt in range(max_retries):
            try:
                response = client.models.generate_content(
                    model=model,
                    contents=prompt,
                    config=types.GenerateContentConfig(temperature=temperature),
                )
                print(f"  ✅ {model} 응답 성공")
                return response.text
            except Exception as e:
                err = str(e)
                if '429' in err or 'RESOURCE_EXHAUSTED' in err:
                    wait = 50 * (attempt + 1)
                    print(f"  ⏳ {model} 속도제한. {wait}초 대기 후 재시도 ({attempt+1}/{max_retries})")
                    time.sleep(wait)
                elif '503' in err or 'UNAVAILABLE' in err:
                    wait = 15 * (attempt + 1)
                    print(f"  ⏳ {model} 과부하. {wait}초 후 재시도 ({attempt+1}/{max_retries})")
                    time.sleep(wait)
                elif '404' in err or 'NOT_FOUND' in err:
                    print(f"  ⚠️ {model} 없음 → 다음 모델로 전환")
                    break
                else:
                    return f"Gemini API 오류: {err}"
    return "❌ API 호출 실패. API 키와 플랜을 확인해주세요."


